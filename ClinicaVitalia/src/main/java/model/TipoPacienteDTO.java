package model;

public class TipoPacienteDTO {
	private int codTipoPaciente;
	private String desTipoPaciente;
	
	public int getCodTipoPaciente() {
		return codTipoPaciente;
	}
	public void setCodTipoPaciente(int codTipoPaciente) {
		this.codTipoPaciente = codTipoPaciente;
	}
	public String getDesTipoPaciente() {
		return desTipoPaciente;
	}
	public void setDesTipoPaciente(String desTipoPaciente) {
		this.desTipoPaciente = desTipoPaciente;
	}
	

}
